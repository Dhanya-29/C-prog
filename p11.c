#include <stdio.h>
#include <string.h>

int main()
{
    int a=5;
    char char1='K';
    float b=5.5;
    long int c=55;
    unsigned long int d=555;
    printf("integer is %d\n",a);
    printf("char is %c\n",char1);
    printf("floating point is %f\n",b);
    printf("long integer is %ld\n",c);
    printf("unsigned longint is %lu\n",d);
    return 0;
}
